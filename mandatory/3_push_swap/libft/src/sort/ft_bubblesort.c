#include "../../includes/libft.h"

void	ft_bubblesort(int *a, int b)
{
	int	i;
	int	j;

	i = 0;
	while (i < b - 1)
	{
		j = 0;
		while (j < ((b - i) - 1))
		{
			if (*(a + j) > *(a + j + 1))
				ft_swap(&a[j], &a[j + 1]);
			j++;
		}
		i++;
	}
}
