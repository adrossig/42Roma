#include "../../includes/libft.h"

void	ft_insertsort(int *a, int b)
{
	int	i;
	int	j;
	int	k;

	i = 1;
	while (i < b)
	{
		k = a[i];
		j = i - 1;
		while (j >= 0 && *(a + j) > k)
		{
			*(a + j + 1) = *(a + j);
			j = j - 1;
		}
		*(a + j + 1) = k;
		i++;
	}
}
